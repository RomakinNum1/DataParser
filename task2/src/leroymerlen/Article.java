package leroymerlen;

public class Article
{
    public String plus;
    public String minus;
    public String message;
    public String name;
    public String product;

    public Article(String plus, String minus, String message, String name, String product)
    {
        this.plus = plus;
        this.minus = minus;
        this.message = message;
        this.name = name;
        this.product = product;
    }
}
