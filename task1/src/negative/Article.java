package negative;

public class Article
{
    public String plus;
    public String minus;
    public String message;
    public String name;
    public String shop;

    public Article(String plus, String minus, String message, String name, String shop)
    {
        this.plus = plus;
        this.minus = minus;
        this.message = message;
        this.name = name;
        this.shop = shop;
    }
}
